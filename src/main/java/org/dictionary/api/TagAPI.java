package org.dictionary.api;

import java.util.Objects;

public class TagAPI {

    private Long id;
    private String name;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }

        TagAPI tagAPI = (TagAPI) o;

        if ( ! Objects.equals(id, tagAPI.id)) return false;

        return true;
    }

    @Override
    public int hashCode() {
        return Objects.hashCode(id);
    }

    @Override
    public String toString() {
        return "TagAPI{" +
            "id=" + id +
            ", name='" + name + "'" +
            '}';
    }
}
